CREATE DATABASE agricola DEFAULT CHARACTER SET utf8;
USE agricola;
CREATE TABLE cards (cardid int NOT NULL PRIMARY KEY,
						deck varchar(10),
						cardname varchar(255),
						cardtext text,
						cost varchar(255),
						requirement varchar(255),
						move boolean,
						type varchar(255));

CREATE TABLE users (userid varchar(255) NOT NULL PRIMARY KEY,
						username varchar(255),
						bajid int);

CREATE TABLE games (gameid int NOT NULL PRIMARY KEY,
						gamename varchar(255),
						gametext text,
						winnerid varchar(255),
						FOREIGN KEY(winnerid) REFERENCES users(userid));

CREATE TABLE stats (gameid int NOT NULL,
						userid varchar(255),
						playcards text,
						score int,
						fields int,
						pastures int,
						grain int,
						vegetables int,
						sheeps int,
						wildboars int,
						cattles int,
						unusedspace int,
						fencedstables int,
						rooms int,
						familymembers int,
						pointcards int,
						specialpoints int,
						PRIMARY KEY(gameid, userid),
						FOREIGN KEY(gameid) REFERENCES games(gameid),
						FOREIGN KEY(userid) REFERENCES users(userid));

